function augmentMinProductsPerLeaf(seed) {
  const list = [...seed]
  const keyOf = (m, mid, l) => `${m}/${mid}/${l}`
  const counts = new Map()
  for (const p of list) {
    const k = keyOf(p.catMain, p.catMid, p.catLeaf)
    counts.set(k, (counts.get(k) || 0) + 1)
  }
  for (const root of categoryRoots) {
    for (const mid of root.children) {
      for (const leaf of mid.children) {
        const k = keyOf(root.slug, mid.slug, leaf.slug)
        const n = counts.get(k) || 0
        for (let i = n; i < 3; i++) {
          list.push({
            slug: `stock-${root.slug}-${mid.slug}-${leaf.slug}-${i}`,
            name: `${leaf.name.toUpperCase()} · PROGRAM ${i + 1}`,
            blurb: `${mid.name} — ${leaf.name}. Club colours, embellishment & bulk tiers on request.`,
            catMain: root.slug,
            catMid: mid.slug,
            catLeaf: leaf.slug,
            isNew: false,
            isBestseller: false,
            image: getShopLeafImage(root.slug, mid.slug, leaf.slug),
            moq: 'B2B program',
          })
        }
      }
    }
  }
  return list
}

export const products = augmentMinProductsPerLeaf(seedProducts)

export function getProduct(slug) {
  return products.find((p) => p.slug === slug)
}

export function filterProducts({ catMain, catMid, catLeaf, bestseller, newOnly, search } = {}) {
  return products.filter((p) => {
    if (catMain && p.catMain !== catMain) return false
    if (catMid && p.catMid !== catMid) return false
    if (catLeaf && p.catLeaf !== catLeaf) return false
    if (bestseller && !p.isBestseller) return false
    if (newOnly && !p.isNew) return false
    if (search) {
      const q = search.toLowerCase().trim()
      if (!q) return true
      if (!p.name.toLowerCase().includes(q) && !p.blurb.toLowerCase().includes(q)) return false
    }
    return true
  })
}

export function countForCatMain(mainSlug) {
  return products.filter((p) => p.catMain === mainSlug).length
}

export function countForShopFilter(mainSlug, midSlug, leafSlug) {
  return filterProducts({ catMain: mainSlug, catMid: midSlug, catLeaf: leafSlug }).length
}
